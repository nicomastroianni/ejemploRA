package ar.com.supervielle.api.entidades;

public class Vinculos {

	private Integer id_persona_fisica;
	private Integer porcentaje_del_vinculo;
	private Integer vinculo;
	public Integer getId_persona_fisica() {
		return id_persona_fisica;
	}
	public void setId_persona_fisica(Integer id_persona_fisica) {
		this.id_persona_fisica = id_persona_fisica;
	}
	public Integer getPorcentaje_del_vinculo() {
		return porcentaje_del_vinculo;
	}
	public void setPorcentaje_del_vinculo(Integer porcentaje_del_vinculo) {
		this.porcentaje_del_vinculo = porcentaje_del_vinculo;
	}
	public Integer getVinculo() {
		return vinculo;
	}
	public void setVinculo(Integer vinculo) {
		this.vinculo = vinculo;
	}
}
